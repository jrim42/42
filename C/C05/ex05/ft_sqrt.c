int	ft_sqrt(int nb)
{
	int	rt;

	rt = 0;
	if (nb <= 0)
		return (0);
	while (rt * rt < nb && rt < 46341)
		rt++;
	if (rt * rt == nb)
		return (rt);
	else
		return (0);
}
